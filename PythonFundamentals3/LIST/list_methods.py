num =  [1, 2, 3]

# apend(val) - add value at the end
num.append(4)
print(num)

# insert(ind, val) - insert value at any index
num.insert(1, 4)
print(num)

# sort() - arrange in increasing order
num.sort()
print(num)

# sort(reverse=true) - decreasing order
num.sort(reverse=True)

