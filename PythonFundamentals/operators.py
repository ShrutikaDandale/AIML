# Arithmetic Operators
a = 2 
b = 3 
print(a+b) # addition
print(a-b) # subtraction    
print(a*b) # multiplication
print(a/b) # division
print(a%b) # modulus
print(a**b) # exponentiation (a to the power b)

# relational operators
print(a==b) # equal to
print(a!=b) # not equal to      
print(a>b) # greater than
print(a<b) # less than
print(a>=b) # greater than or equal to
print(a<=b) # less than or equal to

# assignment operators
a = 5
a += 2 # a = a + 2
print(a)

# logical operators
x = True    
print(not (5 > 8)) # answer is true but not reverse the answer
print((5 > 3) and (2 < 4)) # both conditions are true
print((5 > 3) or (2 > 4)) # at least one condition is true